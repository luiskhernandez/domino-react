let match = function match(param) {
  let value;
  switch(param){
    case 0:
      value = "blank";
      break;
    case 1:
      value = "one";
      break
    case 2:
      value = "two";
      break
    case 3:
      value = "three";
      break
    case 4:
      value = "four";
      break
    case 5:
      value = "five";
      break
    case 6:
      value = "six";
      break
  }
  return value;
}

export default match;
